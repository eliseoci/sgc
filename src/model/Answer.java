package model;

/**
 *
 * @author winvm
 */
public class Answer {

    private int id;
    private String description;
    private double value;

    public Answer() {
    }

    public Answer(int id, String description, double value) {
        this.id = id;
        this.description = description;
        this.value = value;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public double getValue() {
        return value;
    }

    public void setValue(double value) {
        this.value = value;
    }

    @Override
    public String toString() {
        return description;
    }

}
